n = input('Digite Algo: ')

print('É Maiusculas ? {}'.format(n.isupper())) #Verifica se é maiuscula
print('É Numero ? {}'.format(n.isnumeric())) #Verifica se é um numero
print('É Letra ? {}'.format(n.isalpha())) #Verifica se é letra
print('É AlfaNumerico ? {}'.format(n.isalnum())) #Verifica se é alfa numerico