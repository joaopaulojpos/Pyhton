#Sistemas de cálculos

x = int(input('Digite o primeiro valor: '))
y = int(input('Digite o segundo valor: '))

print('A soma de {} e {} é {}'.format(x, y, x + y))
print('A Subtração de {} e {} é {}'.format(x, y, x - y))
print('A Multiplicação de {} e {} é {}'.format(x, y, x * y))
print('A Divisão de {} e {} é {}'.format(x, y, x / y))