n1 = 10
n2 = 4

resultado = n1 + n2
print('Soma ', resultado)

resultado = n1 - n2
print('Subtração ', resultado)

resultado = n1 * n2
print('Multiplicação ', resultado)

resultado = n1 / n2 #Retorna um numero flutuante
print('Divisão ', resultado)


resultado = n1 // n2 #Retorna um numero inteiro descartando a parte fracionária
print('Divisão ', resultado)

resultado = n1 ** n2
print('Expoencial ', resultado) #Retorna um numero e seu expoencial (Potencia)

resultado = n1 % n2
print('Resto ', resultado) #Retorna o resto da divisão